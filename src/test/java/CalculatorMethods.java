public class CalculatorMethods {

    public int getMultiply(int num1,int num2){

        return num1 * num2;
    }
    public int getAddition(int num1,int num2){
        return num1 + num2;
    }

    public int getSubtraction(int num1,int num2){
        return num1 - num2;
    }
    public int getRemainder (int num1,int num2){
        return num1/num2;
    }
}
